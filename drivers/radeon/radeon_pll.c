#define PLL_NUM_CLOCKS (sizeof(fbee_pll) / sizeof(fbee_pll[0]))


static unsigned long get_timer(void)
{
    return * (volatile unsigned long *) 0x4ba;
}

#define SYSTEM_CLOCK    264UL
#define PLL_TIMEOUT     (1000 * SYSTEM_CLOCK)

void radeon_wait_pll(void)
{
    unsigned long timeout = Supexec(get_timer);

    while (*FBEE_VIDEO_PLL_RECONFIG < 0)
    {
        if (Supexec(get_timer) - timeout >= PLL_TIMEOUT)
        {
            // KDEBUG("wait for PLL not busy timed out\r\n");
            break;
        }
    }
}


int radeon_pll_clock_count(void)
{
    return PLL_NUM_CLOCKS;
}

int fbee_pll_clock_freq(int id, bool is_ntsc, unsigned long *freq)
{
    int type = is_ntsc ? SAGA_PLL_NTSC : SAGA_PLL_PAL;

    if (id < 0 || id >= PLL_NUM_CLOCKS)
        return -1;

    *freq = fbee_pll[id].freq[type];

    return 0;
}

int radeon_pll_clock_lookup(bool is_ntsc, unsigned long *freqp)
{
    int type = is_ntsc ? SAGA_PLL_NTSC : SAGA_PLL_PAL;
    int i;
    unsigned long freq;

    if (!freqp)
        return -1;

    freq = *freqp;

    /* Find the closest clock */
    for (i = 0; i < PLL_NUM_CLOCKS-1; i++) {
        unsigned long split;

        if (freq <= fbee_pll[i].freq[type])
            break;

        split = (fbee_pll[i].freq[type] + fbee_pll[i+1].freq[type])/2;

        if (freq < split)
            break;
    }

    *freqp = fbee_pll[i].freq[type];

    /* If we didn't find a match, we will return the largest
     * valid clock.
     */
    return i;
}

int radeon_pll_clock_program(int clock)
{
    int i;

    if (clock < 0 || clock >= PLL_NUM_CLOCKS)
         return -1;

    for (i = 0; i < 18; i++) {
        unsigned char byte = fbee_pll[clock].data[17 - i];
        int j;

        Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                                 SAGA_VIDEO_PLLW_CS(0) |
                                 SAGA_VIDEO_PLLW_CLK(0));

        for (j = 0; j < 8; j++, byte >>= 1) {
            Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                                     SAGA_VIDEO_PLLW_MOSI(byte & 1) |
                                     SAGA_VIDEO_PLLW_CS(0) |
                                     SAGA_VIDEO_PLLW_CLK(0));
            Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                                     SAGA_VIDEO_PLLW_MOSI(byte & 1) |
                                     SAGA_VIDEO_PLLW_CS(0) |
                                     SAGA_VIDEO_PLLW_CLK(1));
        }
    }

    Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                             SAGA_VIDEO_PLLW_CS(1) |
                             SAGA_VIDEO_PLLW_CLK(0));
    Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                             SAGA_VIDEO_PLLW_CS(1) |
                             SAGA_VIDEO_PLLW_CLK(1));
    Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                             SAGA_VIDEO_PLLW_CS(1) |
                             SAGA_VIDEO_PLLW_CLK(0) |
                             SAGA_VIDEO_PLLW_UPDATE(1));
    Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                             SAGA_VIDEO_PLLW_CS(1) |
                             SAGA_VIDEO_PLLW_CLK(1) |
                             SAGA_VIDEO_PLLW_UPDATE(1));
    /* Send 128 clock cycles to allow the PLL to update */
    for (i = 0; i < 128; i++) {
        Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                                 SAGA_VIDEO_PLLW_CS(1) |
                                 SAGA_VIDEO_PLLW_CLK(0));
        Write32(SAGA_VIDEO_PLLW, SAGA_VIDEO_PLLW_MAGIC |
                                 SAGA_VIDEO_PLLW_CS(1) |
                                 SAGA_VIDEO_PLLW_CLK(1));
    }

    return 0;
}

